// رابط التلاوة (مقرئ مشاري العفاسي)
const baseURL = "https://server8.mp3quran.net/afs/";
const surahSelect = document.getElementById("surah");
const player = document.getElementById("player");
// تحميل أسماء السور في القائمة
surahs.forEach((name, i) => {
  const option = document.createElement("option");
  option.value = (i+1).toString().padStart(3, '0') + ".mp3";
  option.textContent = name;
  surahSelect.appendChild(option);
});
// عند اختيار السورة
surahSelect.addEventListener("change", () => {
  player.src = baseURL + surahSelect.value;
  player.play();
});